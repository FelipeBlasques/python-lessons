# strings

var = 'ABC'
print(f'{var}.')
print(f'{var: >10}.')
print(f'{var: <10}.')
print(f'{var: ^10}.')
print(f'{var:0^10}.')
print(f'{var:#^10}.')

print(10 * '-')

print(f'{-1000.4214645126:,.1f}')
print(f'{1000.4214645126:+,.2f}')
print(f'{1000.4214645126:0=+10,.4f}')

print(10 * '-')

print(f'The hex of 1500 is {1500:08X}')

print(10 * '-')

print(f'{var!r}')
print(f'{var!s}')
print(f'{var!a}')

