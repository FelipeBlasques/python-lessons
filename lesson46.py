# range + for

num = range(10)
num2 = range(5, 10)
num3 = range(5, 10, 2)
print(num)
print(num2)
print(num3)
print('---------')

for number in num:
    print(number)
print('---------')

for number2 in num2:
    print(number2)
print('---------')

for number3 in num3:
    print(number3)
print('---------')